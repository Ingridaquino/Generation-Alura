package br.com.generation.aritmetico;

public class Operadores {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;

        int soma = num1 + num2;
        int subtracao = num1 - num2;
        int mult = num1 * num2;
        int divi = num2 / num1;
        int resto = num2 % num1;

        System.out.println("O valor da soma é: " + soma);
        System.out.println("O valor da subtração é: " + subtracao);
        System.out.println("O valor da multiplicação é: " + mult);
        System.out.println("O valor da divisão é: " + divi);
        System.out.println("O valor do resto é: " + resto);


    }
}
