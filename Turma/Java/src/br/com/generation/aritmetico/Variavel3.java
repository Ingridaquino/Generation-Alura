package br.com.generation.aritmetico;

public class Variavel3 {
    public static void main(String[] args) {
        int var1 = 10;
        double var2 = 10.0;

        System.out.println("Valor original da 1° variável: " + var1);
        System.out.println("Valor original da 2° variável: " + var2);
        System.out.println();

        var1 = var1 / 4;
        var2 = var2 / 4;

        System.out.println("Valor original da 1° variável após a divisão: " + var1);
        System.out.println("Valor original da 2° variável após a divisão  : " + var2);
    }
}
