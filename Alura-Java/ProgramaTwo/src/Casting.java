public class Casting {
    public static  void main(String[] args){
        double salario = 2345.55;
        int total = (int) salario; // O casting para forçar a transformação do double em Int
        System.out.println(total);

        double valor1 = 0.2;
        double valor2 = 0.1;
        double totall = valor1 + valor2;

        System.out.println(totall);
    }
}
